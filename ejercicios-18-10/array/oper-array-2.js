let frutas = ["manzana", "pera", "uva", "fresa"];

frutas.push("kiwi");

//Agregar un elemento al inicio del array
frutas.unshift("naranja");
console.log(frutas);

//Elimina el elemento inicial del array
frutas.shift();
console.log(frutas);

//Reordenar el array inversamente
frutas.reverse();
console.log(frutas);

//Ordena el array alfabeticamente
frutas.sort();
console.log(frutas);

